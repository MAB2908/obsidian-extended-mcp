async function main() {
  const proc = spawn('node', ['dist/index.js'], {
    cwd: rootDir,
    env,
    stdio: ['pipe', 'pipe', 'pipe'],
  });

  // Wait for startup message
  await new Promise((resolve) => {
    proc.stdout.once('data', resolve);
  });

  console.log('Requesting tools/list...');
  const listResponse = await sendRequest(proc, { jsonrpc: '2.0', id: 1, method: 'tools/list' });
  const tools = listResponse.result?.tools || [];
  console.log(`Total tools registered: ${tools.length}`);

  const results = [];
  let passed = 0;
  let failed = 0;
  let skipped = 0;

  for (let i = 0; i < tools.length; i++) {
    const tool = tools[i];
    const reqId = i + 2;
    const args = generateArgs(tool);

    try {
      const callResponse = await sendRequest(proc, {
        jsonrpc: '2.0',
        id: reqId,
        method: 'tools/call',
        params: { name: tool.name, arguments: args },
      });

      if (callResponse.error) {
        // JSON-RPC error means server handled it but returned error
        results.push({ name: tool.name, status: 'RPC_ERROR', error: callResponse.error.message });
        failed++;
      } else {
        const result = callResponse.result;
        if (result && result.isError) {
          // Tool returned business error (e.g. CLI unavailable, policy blocked)
          const text = result.content?.[0]?.text || '';
          results.push({ name: tool.name, status: 'TOOL_ERROR', error: text.slice(0, 100) });
          // Count as passed if it's expected (security/policy/dependency)
          if (text.includes('disabled by security policy') ||
              text.includes('CLI unavailable') ||
              text.includes('not enabled') ||
              text.includes('not initialized') ||
              text.includes('not available') ||
              text.includes('not found') ||
              text.includes('disabled') ||
              text.includes('unavailable')) {
            passed++;
          } else {
            failed++;
          }
        } else {
          results.push({ name: tool.name, status: 'OK' });
          passed++;
        }
      }
    } catch (err) {
      results.push({ name: tool.name, status: 'CRASH', error: err.message });
      failed++;
    }
  }

  proc.kill();

  console.log('\n--- Smoke Test Results ---');
  console.log(`Total: ${tools.length}, OK: ${passed}, Failed: ${failed}, Skipped: ${skipped}`);
  console.log('\nFailed/Crashed tools:');
  for (const r of results) {
    if (r.status === 'CRASH' || (r.status === 'TOOL_ERROR' && !r.error?.includes('disabled') && !r.error?.includes('unavailable'))) {
      console.log(`  ${r.name}: ${r.status} — ${r.error || ''}`);
    }
  }

  process.exit(failed > 0 ? 1 : 0);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
